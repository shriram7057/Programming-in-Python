#Give input from user 
a=int (input("Enter a Value of a :"))
b=int (input("Enter a Value of b :"))

#Addition value of left operand and the right operand
addition=a+b
print("Addition of a and b=",addition)


#subtraction -subtract the value of the left operand from the value of the right operand
subtraction=a-b
print("Subtraction of a and b=",subtraction)

#Multiplication- Multiply the value of left and the right operator
Multiplication=a*b
print("Multiplication of a and b=",Multiplication)

#Division- Divide the value of the left operand to the value of right operand
Division=a/b
print("Division of a and b=",Division)

#floor Division -Gives the quotient without Decimal 
Floor_Division=a//b
print("Floor Division of a and b=",Floor_Division)

#Modulus - Gives Remainder 
Modulus=a%b
print("modulus of a and b=",Modulus)

#Exponentiation - power operation perform exponential Calculations 

exponentiation=a**b
print("exponentiation of a and b=",exponentiation)