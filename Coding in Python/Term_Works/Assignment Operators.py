# Assignment Operators (Augmented Assignment Operators) in Python

# Initialize a variable
x = 10  
print("Initial value of x:", x)

# Addition assignment   It adds right operand to the left operand and assign the result to the left operand
x += 5 
print("Addition assignment After x += 5  :", x)  # Result: 15

# Subtraction assignment   It subtracts right operand from the left operand and assign the result to the left operand
x -= 3  
print("Subtraction assignment After x -= 3  :", x)  # Result: 12

# Multiplication assignment   It multiply right operand with the left operand and assign the result to the left operand
x *= 2
print("Multiplication assignment After x *= 2  :", x)  # Result: 24

# Division assignment  It divide left operand by the right operand and assign result to the left operand    
x /= 4  
print("Division assignment After x /= 4  :", x)  # Result: 6.0

# Modulus assignment  it takes the modulus of the two operands and assign result to the left operand
x %= 5  
print("Modulus assignment After x %= 5  :", x)  # Result: 1.0

# Exponentiation assignment  perform Exponentiation power calculations on operators
x **= 3 
print("Exponentiation assignment After x **= 3 :", x)  # Result: 1.0

# Floor division assignment   perform Floor Division operation on two operands 
x //= 2 
print("Floo Division assignment After x //= 2 :", x)  # Result: 0.0
