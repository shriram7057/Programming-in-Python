# Python Programs to demonstrate arithmetic operators
#input : Two numbers 
a=10
b=5

# Addition +  Adds the value of the left and right operands
addition=a+b
print("Addition of a and b=",addition)

#Subtraction -  Subtracts the value of the rightoperand from the value of the left operand
sub=a-b
print("Subtraction of a and b=",sub)

#Multiplication *  Multiply the values of left and rightoperands
Multiplication=a*b
print("Multiplication of a and b=",Multiplication)

#Division / Dividesthe  value of the left operand by the value of right operand
Division=a/b
print("Division of a and b=",Division)

# Floor Division // -> Gives quotient without decimal 
floor_division=a//b
print("Floor Division of a and b=",floor_division)

#Modulus % -> Gives Remainder
modulus=a%b
print("modulus of a and b=",modulus)

# Exponentiation ** -> power operation  Performsexponential calculation, raises first operand to the second operand
exponentiation=a**b
print("Exponentiation of a and b=",exponentiation)
