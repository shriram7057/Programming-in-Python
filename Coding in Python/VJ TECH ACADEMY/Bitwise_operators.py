# Bitwise Operator Demonstration in Python

# Define variables
x = 10  
y = 4   

# Perform bitwise operations and display results

print("Bitwise AND (x & y):", x & y)  # AND operation 
print("Bitwise OR (x | y):", x | y)   # OR operation
print("Bitwise XOR (x ^ y):", x ^ y)  # XOR operation 
print("Bitwise NOT (~x):", ~x)       # NOT operation
print("Left Shift (x << 2):", x << 2) # Left shift 
print("Right Shift (x >> 2):", x >> 2) # Right shift
