a = [10, 20, 30, 40, 50, 60]  # Define a list

print(a[1])   # Print the second element (index starts from 0, so index 1 is 20)

print(a[2:])  # Print elements from index 2 to the end

print(a[:3])  # Print the first three elements 

print(a[-3])  # Print the third element from the end 

print(a[0:3]) # Print elements from index 0 to 2 

print(a[5:])  # Print elements from index 5 to the end 
