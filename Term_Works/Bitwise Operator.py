# Bitwise Operator Demonstration in Python

# Define variables
x = 10  # Binary: 1010
y = 4   # Binary: 0100           

# Perform bitwise operations and display results
print("Bitwise AND (x & y):", x & y)  # AND operation   if both bits are 1 then result is 1 otherwise 0
print("Bitwise OR (x | y):", x | y)   # OR operation    result is 14 in binary 1114
print("Bitwise XOR (x ^ y):", x ^ y)  # XOR operation   if both bits are same then result is 0 otherwise 0
print("Bitwise NOT (~x):", ~x)        # NOT operation   inverts of each bits 
print("Left Shift (x << 2):", x << 1) # Left shift      
print("Right Shift (x >> 2):", x >> 1) # Right shift
