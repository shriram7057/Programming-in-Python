
#Logical operators
a=int (input("Enter a Value of a "))
b=int (input("Enter a Value of b "))

print("Logical operators in python!")

print("AND Operation",a>b and a<b)   # if Both operand is true result is true 

print("OR Operation",a>b or a<b)    # if any two operand are non Zero the condition is true

print("NOT Operation",(a>b))        # inverts of each bit