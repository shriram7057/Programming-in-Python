# Using 'in' and 'not in' with List
my_list = [10, 20, 30, 40, 50]
print(30 in my_list)  # True
print(100 not in my_list)  # True

# Using 'in' and 'not in' with String
my_string = "Hello, world!"
print("world" in my_string)  # True
print("Python" not in my_string)  # True

# Using 'in' and 'not in' with Dictionary
my_dict = {"name": "Alice", "age": 25}
print("age" in my_dict)  # True
print("salary" not in my_dict)  # True
